-- ==============================================
-- IceNvim 核心配置入口文件：批量加载所有核心功能模块
-- 作用：统一管理核心配置的加载顺序，确保各模块按依赖关系正确初始化
-- 加载逻辑：按「基础设置→工具命令→文件配置→快捷键→杂项→图标」的顺序加载
-- 新手说明：每个模块对应 lua/core/ 目录下的文件，以下详细解释每个模块的功能、特性和用途
-- ==============================================

-- 1. 加载基础配置模块：core/basic.lua
-- 核心定位：Neovim 全局基础功能的总开关，所有核心显示、操作规则的基础
-- 详细功能与特性：
-- - 编码设置：强制使用 UTF-8 编码（避免中文乱码），同时配置文件编码与全局编码一致
-- - 显示优化：启用行号+相对行号（高效移动）、光标行高亮（快速定位光标）、符号列（显示图标）、80列参考线（规范行宽）
-- - 缩进规则：Tab 转 4 空格、自动缩进对齐、智能缩进级别（避免不规则缩进）
-- - 搜索增强：忽略大小写+智能大小写匹配（模糊搜索与精确搜索兼顾）、关闭搜索高亮（减少干扰）
-- - 窗口操作：水平/垂直分屏默认在下方/右侧（符合直觉）、滚动偏移（保留上下文）、禁用自动换行（代码不拆分）
-- - 编辑体验：启用鼠标支持（所有模式）、允许隐藏已修改缓冲区（多文件编辑更灵活）、外部文件修改自动读取（避免编辑旧内容）
-- - 性能优化：禁用备份文件/交换文件（减少磁盘占用）、设置快捷键超时时间（避免误触）
-- 注意点：基础配置是所有功能的前提，修改需谨慎，新手建议保持默认
require "core.basic"

-- 2. 加载自定义命令模块：core/commands.lua
-- 核心定位：封装常用功能为 :命令 形式，新手无需记复杂快捷键，直接输入命令即可使用
-- 详细功能与特性（7个核心命令）：
-- - :IceAbout：查看配置信息（作者、仓库地址、版权），快速了解配置来源
-- - :IceCheckIcons：验证 Nerd Font 图标是否正常加载（避免图标乱码），显示所有配置的图标及名称
-- - :IceCheckPlugins：检查插件更新状态（标记超过30天未更新的插件），严重过时（>365天）标红、普通过时标黄
-- - :IceUpdate：更新 IceNvim 配置本身（通过 git pull 拉取最新代码），一键获取新功能和修复
-- - :IceHealth：检查配置依赖（如 curl、gcc、Python 等），快速排查功能失效的原因
-- - :IceRepeat：重复执行指定命令（支持计数，如 3:IceRepeat w 表示保存3次），提升重复操作效率
-- - :IceView：在独立缓冲区查看命令输出（如 :IceView set 查看所有配置），解决长输出显示不全问题
-- 注意点：所有命令均支持普通模式输入 :命令名 回车执行，部分命令需配合其他工具（如 git、Nerd Font）
require "core.commands"

-- 3. 加载文件类型配置模块：core/ft.lua
-- 核心定位：为不同编程语言/文件类型设置专属规则，实现「一键切换编辑环境」
-- 详细功能与特性（支持10种常见文件类型，含扩展方法）：
-- - C (.c)：Tab 键对应 2 空格（适配部分 C 项目编码习惯）
-- - C# (.cs)：80列参考线改为 100 列（适配长类名/方法名）
-- - CSS (.css)：配置注释格式为 /* 注释 */（支持 gcc/gc 快捷键快速注释）
-- - Dart (.dart)：Tab 键对应 2 空格（符合 Flutter 官方推荐规范）
-- - HTML (.html)：自动换行+按单词换行+缩进换行（提升标签嵌套可读性），参考线 120 列
-- - JavaScript (.js)：参考线 120 列，注释格式为 // 注释（支持快速单行注释）
-- - Markdown (.md)：自动换行+按单词换行+缩进换行（优化文档阅读体验）
-- - Python (.py)：参考线 88 列（符合 black 格式化工具规范），优化注释和缩进规则（自动对齐注释、换行缩进）
-- - TypeScript (.ts)：参考线 120 列，注释格式为 // 注释（与 JS 保持一致）
-- - Typst (.typ)：自动换行+按单词换行+缩进换行，Tab 2 空格，参考线 120 列（适配排版工具需求）
-- - 扩展方法：Ice.ft:set(文件类型, 配置函数)（不覆盖原有配置，新增自定义规则）
-- 注意点：配置自动生效（打开对应文件即应用），新手可通过扩展方法添加自己需要的文件类型规则
require "core.ft"

-- 4. 加载快捷键配置模块：core/keymap.lua
-- 核心定位：自定义 Neovim 快捷键，替代默认繁琐操作，提升编辑效率
-- 详细功能与特性（核心快捷键分类）：
-- - 窗口操作：分屏创建（水平/垂直）、分屏切换、窗口大小调整、窗口关闭（统一用 <leader> + 快捷键）
-- - 缓冲区操作：缓冲区切换（上一个/下一个）、缓冲区关闭、缓冲区列表查看（无需手动输入 :bnext/:bprev）
-- - 编辑操作：快速保存（<leader>w）、快速退出（<leader>q）、强制退出（<leader>Q）、撤销/重做、复制/粘贴（适配系统剪贴板）
-- - 搜索操作：全局搜索（<leader>f）、文件内搜索（<leader>/）、搜索结果跳转（Next/Prev）
-- - 代码操作：代码折叠/展开、注释/取消注释（gcc 单行注释、gc 可视化块注释）、代码格式化
-- - 插件快捷键：为常用插件（如 Telescope、Nvim-Tree）绑定专属快捷键（如 <leader>e 打开文件树）
-- 注意点：
-- - <leader> 是快捷键前缀（默认是空格，可在模块中修改），所有自定义快捷键都基于 <leader> 避免冲突
-- - 新手建议先熟悉核心快捷键（保存、退出、分屏、搜索），再逐步探索其他功能
-- - 支持自定义修改：直接在 core/keymap.lua 中调整快捷键绑定，或通过 custom 目录扩展
require "core.keymap"

-- 5. 加载杂项配置模块：core/miscellaneous.lua
-- 核心定位：存储无法归类到其他模块的辅助功能，补充核心配置的细节体验
-- 详细功能与特性（辅助功能合集）：
-- - 自动命令：如终端窗口关闭行号、命令历史窗口自动进入插入模式、新建窗口禁用自动换行等
-- - 功能开关：如禁用内置插件（避免冲突）、启用某些 Neovim 实验性功能（提升体验）
-- - 行为优化：如光标样式调整、鼠标滚轮速度、命令行自动补全优化等
-- - 兼容性处理：解决不同系统（Linux/Mac/Windows）的细节差异（如路径分隔符、剪贴板适配）
-- 注意点：该模块功能较杂，但都是提升使用体验的细节，新手无需修改，若有特殊需求可针对性调整
require "core.miscellaneous"

-- 6. 加载图标配置模块：core/symbols.lua
-- 核心定位：统一管理所有插件和配置中使用的 Nerd Font 图标，确保图标风格一致
-- 详细功能与特性：
-- - 图标分类：包含错误（error）、警告（warning）、信息（info）、提示（hint）、Git 操作（add/modify/delete）、文件类型（file/dir）等常用图标
-- - 统一调用：所有插件（如 LSP 诊断、Telescope、Bufferline）都从该模块获取图标，修改时只需改一处
-- - 兼容性：使用 Nerd Font 标准图标，确保安装任意一款 Nerd Font 都能正常显示
-- 注意点：
-- - 必须安装 Nerd Font（如 JetBrains Mono Nerd Font、FiraCode Nerd Font），否则图标会显示为方框/乱码
-- - 可通过 :IceCheckIcons 命令验证图标是否正常加载，若显示异常需检查字体安装和终端字体设置
require "core.symbols"